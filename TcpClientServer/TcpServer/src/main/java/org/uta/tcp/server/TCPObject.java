package org.uta.tcp.server;

public class TCPObject {

	private long time;
	private String tag;
	
	
	public long getTime() {
		return time;
	}
	
	public void setTime(long time) {
		this.time = time;
	}
	
	public String getTag() {
		return tag;
	}
	
	public void setTag(String tag) {
		this.tag = tag;
	}	
}
